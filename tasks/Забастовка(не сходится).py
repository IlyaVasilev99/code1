N, K= map(int, input().split())
A=set()
for i in range(K):
    a_i, b_i = map(int, input().split())
    n=a_i
    while n<=N:
        A.add(n)
        if n%6==0 or n%7==0:
            A.remove(n)
        n+=b_i
